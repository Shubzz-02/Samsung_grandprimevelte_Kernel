#ifndef __PXA1908_MEMORY_RESERVE_SIZE_H_
#define __PXA1908_MEMORY_RESERVE_SIZE_H_

#include <dt-bindings/sizes.h>

#define PXA1908_RAMDISK_RESERVE_SIZE 		(2600 * SZ_1K)
#define PXA1908_CP_RESERVE_SIZE			(48 * SZ_1M)
#define PXA1908_SECLOG_RESERVE_SIZE 		(SZ_1M)
#define PXA1908_SECLASTK_LOG_RESERVE_SIZE 	(SZ_1M)
#define PXA1908_SEC_FTRACE_RESERVE_SIZE 	(5 * SZ_1M)
#define PXA1908_GC_RESERVE_SIZE 		(SZ_16M)
#define PXA1908_CM3_SHARED_BUF_SIZE		(512 * SZ_1K)
#define PXA1908_CMA_RESERVE_SIZE 		(8 * SZ_1M)

#define PXA1908_CP_BASE 		(0x05000000)
#define PXA1908_RAMDISK_BASE		(PXA1908_CP_BASE - PXA1908_RAMDISK_RESERVE_SIZE)
#define PXA1908_SECLOG_BASE		(PXA1908_CP_BASE + PXA1908_CP_RESERVE_SIZE)
#define PXA1908_SECLASTK_LOG_BASE	(PXA1908_SECLOG_BASE + PXA1908_SECLOG_RESERVE_SIZE)
#define PXA1908_SEC_FTRACE_BASE		(PXA1908_SECLASTK_LOG_BASE + PXA1908_SECLASTK_LOG_RESERVE_SIZE)
#define PXA1908_DTB_BASE		(PXA1908_SEC_FTRACE_BASE + PXA1908_SEC_FTRACE_RESERVE_SIZE)
#define PXA1908_GC_BASE 		(0x09000000)
#define PXA1908_CM3_SHARED_BUF_BASE	(PXA1908_GC_BASE + PXA1908_GC_RESERVE_SIZE)
#define PXA1908_FB_BASE 		(PXA1908_CM3_SHARED_BUF_BASE + PXA1908_CM3_SHARED_BUF_SIZE)

#endif
