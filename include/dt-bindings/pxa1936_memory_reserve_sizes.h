#define PXA1936_RAMDISK_BASE		(PXA1936_CP_BASE - PXA1936_RAMDISK_RESERVE_SIZE)
#define PXA1936_SECLOG_BASE		(PXA1936_CP_BASE + PXA1936_CP_RESERVE_SIZE)
#define PXA1936_SECLASTK_LOG_BASE	(PXA1936_SECLOG_BASE + PXA1936_SECLOG_RESERVE_SIZE)
#define PXA1936_SEC_FTRACE_BASE		(PXA1936_SECLASTK_LOG_BASE + PXA1936_SECLASTK_LOG_RESERVE_SIZE)
#define PXA1936_DTB_BASE		(PXA1936_SEC_FTRACE_BASE + PXA1936_SEC_FTRACE_RESERVE_SIZE)
#define PXA1936_GC_BASE 		(0x09000000)
#define PXA1936_CM3_SHARED_BUF_BASE	(PXA1936_GC_BASE + PXA1936_GC_RESERVE_SIZE)
#define PXA1936_FB_BASE 		(PXA1936_CM3_SHARED_BUF_BASE + PXA1936_CM3_SHARED_BUF_SIZE)

#endif
